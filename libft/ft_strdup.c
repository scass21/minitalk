#include "libft.h"

char	*ft_strdup(const char *src)
{
	char			*dest;
	size_t			i;
	unsigned long	j;

	i = 0;
	j = ft_strlen(src);
	dest = malloc(sizeof(char) * (j + 1));
	if (!dest)
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
